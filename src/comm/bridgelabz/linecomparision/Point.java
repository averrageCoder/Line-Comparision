package comm.bridgelabz.linecomparision;

public class Point {

	public int xCoordinate, yCoordinate;
	
	public Point(int x, int y) {
		this.xCoordinate = x;
		this.yCoordinate = y;
	}

	public int getYCoordinate() {
		return this.xCoordinate;
	}

	public int getXCoordinate() {
		return this.yCoordinate;
	}
	
}
